    private String getNextAttackType(String a) {
        switch (a) {
            case "Melee":
                return "Range";
            case "Range":
                return "Wisdm";
            case "Wisdm":
                return "Intel";
            case "Intel":
                return "Chrsm";
            case "Chrsm":
                return "None ";
            case "None ":
                return "Melee";
        }
        return "";
    }

    private int getAttackTypeModifier(String a) {
        switch (a) {
            case "Melee":
                return Integer.parseInt(jLabel8.getText());
            case "Range":
                return Integer.parseInt(jLabel9.getText());
            case "Wisdm":
                return Integer.parseInt(jLabel12.getText());
            case "Intel":
                return Integer.parseInt(jLabel11.getText());
            case "Chrsm":
                return Integer.parseInt(jLabel14.getText());
            case "None ":
                return 0;
        }
        return 0;
    }


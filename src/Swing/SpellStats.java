    private void jCheckBox4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox4ActionPerformed
        if (jCheckBox4.isSelected()) {
            jCheckBox4.setText(getNextAttackType(jCheckBox4.getText()));
            jCheckBox4.setSelected(false);
        }
        
        dc=new Formula("8+"+getCurrSpellAttr()+"+PROF");
        atk=new Formula(getCurrSpellAttr()+"+PROF");
        
        updateSpellVars();
    }//GEN-LAST:event_jCheckBox4ActionPerformed

    public void updateSpellVars(){
        jLabel1.setText("Spell DC: "+dc.getValue(g.getModifier("STR"), g.getModifier("DEX"),
                g.getModifier("CON"), g.getModifier("INT"), g.getModifier("WIS"),
                g.getModifier("CHA"), g.getModifier("PROF")));
        
        jLabel2.setText("Atk Bonus: "+atk.getValue(g.getModifier("STR"), g.getModifier("DEX"),
                g.getModifier("CON"), g.getModifier("INT"), g.getModifier("WIS"),
                g.getModifier("CHA"), g.getModifier("PROF")));
    }


    public String getNextAttackType(String a) {
        switch (a) {
            case "Intel":
                return "Wisdm";
            case "Wisdm":
                return "Chrsm";
            case "Chrsm":
                return "Intel";
        }
        return "";
    }
    
    public String getCurrSpellAttr() {
        switch (jCheckBox4.getText()) {
            case "Intel":
                return "INT";
            case "Wisdm":
                return "WIS";
            case "Chrsm":
                return "CHA";
        }
        return "";
    }
    

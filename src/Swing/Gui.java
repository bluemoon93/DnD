    private String getNextAttackType(String a) {
        switch (a) {
            case "Melee":
                return "Range";
            case "Range":
                return "Wisdm";
            case "Wisdm":
                return "Intel";
            case "Intel":
                return "Chrsm";
            case "Chrsm":
                return "None ";
            case "None ":
                return "Melee";
        }
        return "";
    }

    private int getAttackTypeModifier(String a) {
        switch (a) {
            case "Melee":
                return getModifier("STR");
            case "Range":
                return getModifier("DEX");
            case "Wisdm":
                return getModifier("WIS");
            case "Intel":
                return getModifier("INT");
            case "Chrsm":
                return getModifier("CHA");
            case "None ":
                return 0;
        }
        return 0;
    }


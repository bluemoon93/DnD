    public int getModifier(String stat) {
        switch (stat) {
            case "DEX":
                return Integer.parseInt(attributesPanel1.dexModifier.getText());
            case "STR":
                return Integer.parseInt(attributesPanel1.strModifier.getText());
            case "INT":
                return Integer.parseInt(attributesPanel1.intModifier.getText());
            case "CON":
                return Integer.parseInt(attributesPanel1.conModifier.getText());
            case "WIS":
                return Integer.parseInt(attributesPanel1.wisModifier.getText());
            case "CHA":
                return Integer.parseInt(attributesPanel1.chaModifier.getText());
            case "PROF":
                return Integer.parseInt(aCPanel1.profScore.getText());
            default:
                try {
                    return Integer.parseInt(stat);
                } catch (Exception ex) {
                    return 0;
                }
        }
    }

    public void updatePassiveWisdom() {
        int val = 10;
        if (((DicePanel) dicePanel).hasAdvantage.isSelected()) {
            val += 5;
        } else if (((DicePanel) dicePanel).hasDisadvantage.isSelected()) {
            val -= 5;
        }
        aCPanel1.passWisScore.setText((val + getModifier("WIS") + getModifier("PROF")) + "");
    }

